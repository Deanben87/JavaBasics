public class LoopsPractice {

	public static void main(String[] args) {

		int sum = 0;

		for (int i = 1; i < 6; i++) {

			System.out.print(sum +"  ");// 0 1 3 6 10
			sum += i;
			// System.out.print(sum+" ");//1 3 6 10 15

		}

		System.out.println();
		System.out.println(sum);//15
		
		
		
		
		
		
	}

}
